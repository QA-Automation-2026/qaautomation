package Operators2;

public class unaryOperators {



    public static void main(String[] args) {
        int a =10;
        int b =20;
        int c =30;

        System.out.println("pre increment"+ ++a);//pre increment
        System.out.println("pre increment"+ a);
        System.out.println("pre increment"+ ++b);//pre
        System.out.println("pre increment"+b);
        System.out.println("post increment"+a++);//post increment
        System.out.println("post increment"+ a);
        System.out.println("pre decrement"+ c);
        System.out.println("pre decrement"+--c);//pre
        System.out.println("pre decrement"+-c); //decremental
        System.out.println("post decrement"+c--);//post
        System.out.println("post decrement"+c--);//post

    }
}
